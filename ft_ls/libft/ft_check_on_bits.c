#include "libft.h"

size_t	check_on_bits(int num)
{
	size_t	len;
	size_t	c;

	c = 0;
	len = 32;
	while (len)
	{
		c += num & 0x1;
		num >>= 1;
		len--;
	}
	return (c);
}
