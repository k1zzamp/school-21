#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*copy;

	copy = s1;
	while (*copy)
		copy++;
	while (*s2 && n--)
		*copy++ = *s2++;
	*copy = '\0';
	return (s1);
}
