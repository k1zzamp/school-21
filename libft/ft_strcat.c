#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*copy;

	copy = s1;
	while (*copy)
		copy++;
	while (*s2)
		*copy++ = *s2++;
	*copy = '\0';
	return (s1);
}
