#include "libft.h"

int		ft_isspace(int c)
{
	c = (unsigned char)c;
	if (c == '\t' || c == '\n' || c == '\v'
			|| c == '\f' || c == ' ' || c == '\r')
		return (1);
	else
		return (0);
}
